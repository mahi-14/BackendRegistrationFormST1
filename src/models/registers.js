//to define schema->database
const mongoose = require("mongoose"); //importing mongoose

//creating object->key value pair schema
const userSchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  gender: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
    required: true,
    unique: true,
  },
  age: {
    type: Number,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  confirmpassword: {
    type: String,
    required: true,
  },
});

//now we need to create collection-->collection name first letter should be capital
const Register = new mongoose.model("Register", userSchema); //making model

module.exports = Register; //exporting collection
